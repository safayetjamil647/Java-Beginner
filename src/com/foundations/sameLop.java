package com.foundations;

public class sameLop {
    public static void main(String[] args) {
        int x= 0;
        while(x<5)
        {
            System.out.println("hello world");
            x=x+1;
        }
        int y=0;
        do {
            System.out.println("dear bear");
            y++;
        }while(y<5);

        for (int i = 0;i<5;i++)
        {
            System.out.println("hello boss");
        }

    }
}
